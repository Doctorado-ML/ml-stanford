% m = Number of examples
m = size(X, 1);


%% =========== Part 6: Feature Mapping for Polynomial Regression =============
%  One solution to this is to use polynomial regression. You should now
%  complete polyFeatures to map each example into its powers
%

p = 8;

% Map X onto Polynomial Features and Normalize
X_poly = polyFeatures(X, p);
[X_poly, mu, sigma] = featureNormalize(X_poly);  % Normalize
X_poly = [ones(m, 1), X_poly];                   % Add Ones

% Map X_poly_test and normalize (using mu and sigma)
X_poly_test = polyFeatures(Xtest, p);
X_poly_test = bsxfun(@minus, X_poly_test, mu);
X_poly_test = bsxfun(@rdivide, X_poly_test, sigma);
X_poly_test = [ones(size(X_poly_test, 1), 1), X_poly_test];         % Add Ones

% Map X_poly_val and normalize (using mu and sigma)
X_poly_val = polyFeatures(Xval, p);
X_poly_val = bsxfun(@minus, X_poly_val, mu);
X_poly_val = bsxfun(@rdivide, X_poly_val, sigma);
X_poly_val = [ones(size(X_poly_val, 1), 1), X_poly_val];           % Add Ones

fprintf('Normalized Training Example 1:\n');
fprintf('  %f  \n', X_poly(1, :));

times = 50;
error_train = zeros(times, 1);
error_val   = zeros(times, 1);
% execute 50 times
for i = 1:times
    num_elements = randi([1, m], 1);
    % num_elements Tests randomly ordered
    elements = randperm(numel(X), num_elements);
    %Train with lambda selected
    lambda = 3;
    [theta] = trainLinearReg(X_poly(elements, :), y(elements), lambda);
    error_train(i) = linearRegCostFunction(X_poly(elements, :), y(elements), theta, 0);
    error_val(i)   = linearRegCostFunction(X_poly_val(elements, :), yval(elements), theta, 0);
end
printf("Error train      mean %f\n", mean(error_train));
printf("Error validation mean %f\n", mean(error_val));

